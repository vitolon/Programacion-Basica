# Operadores Aritméticos
a = 10
b = 3
suma = a + b      # 10 + 3 = 13
resta = a - b     # 10 - 3 = 7
multiplicacion = a * b  # 10 * 3 = 30
division = a / b  # 10 / 3 = 3.333...
division_entera = a // b  # 10 // 3 = 3
potencia = a ** b  # 10^3 = 1000

print(f"Suma: {suma}, Resta: {resta}, Multiplicación: {multiplicacion}")
print(f"División: {division}, División Entera: {division_entera}, Potencia: {potencia}")

# Operadores Relacionales
print(a > b)   # True
print(a == b)  # False
print(a != b)  # True

# Operadores Lógicos
x = True
y = False
print(x and y)  # False
print(x or y)   # True
print(not x)    # False

# Operadores de Asignación
c = 5
c += 3  # c = c + 3 -> 8
print(c)