#Programa para determinar si puede  un alumno hacer servicio o residencias.

Creditos = int(input("Ingrese sus creditos: "))
if Creditos >= 80:
    print("Eres apto para realizar tu servicio")
else:
    print("Te faltan creditos para realizar tu servicio")
if Creditos >= 120:
    print("Eres apto para realizar tus residencias")
else:
    print("Te faltan creditos para realizar tus residencias")
